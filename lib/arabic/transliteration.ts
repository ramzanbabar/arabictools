const AR2L: Record<string,string> = {
  "ا":"a","أ":"a","إ":"i","آ":"aa","ب":"b","ت":"t","ث":"th","ج":"j",
  "ح":"h","خ":"kh","د":"d","ذ":"dh","ر":"r","ز":"z","س":"s","ش":"sh",
  "ص":"s","ض":"d","ط":"t","ظ":"z","ع":"'","غ":"gh","ف":"f","ق":"q",
  "ك":"k","ل":"l","م":"m","ن":"n","ه":"h","و":"w","ي":"y","ى":"a","ة":"h"," ":" ",
};
export const arabicToLatin = (t: string) => t.split("").map(c => AR2L[c] ?? c).join("");
